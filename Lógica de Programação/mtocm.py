"""
 Faça um programa em linguagem python que receba um valor em metros,
 e o converta para centímetros. Por fim, imprima na tela o resultado obtido.
 Dica: para converter metros em centímetros, basta multiplicar o valor por 100.
"""

m = float(input("Digite um valor (em metros): "))

cm = m * 100

print(f"Conversão metro para centímetros: {cm:.2f} cm")
